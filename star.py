import turtle


turtle.Screen().bgcolor("Orange")

sc = turtle.Screen()
sc.setup(400,300)


turtle.title("Welcome to Turtle Window")

b1=turtle.Turtle()

# Triangle
b1.forward(100)

b1.left(120)
b1.forward(100)

b1.left(120)
b1.forward(100)


b1.penup()
b1.right(150)
b1.forward(50)

b1.pendown()
b1.right(90)
b1.forward(100)
b1.right(120)
b1.forward(100)
b1.right(120)
b1.forward(100)
b1.right(120)
b1.forward(100)








turtle.done()